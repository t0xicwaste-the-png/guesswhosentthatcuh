const messageEl = document.getElementById('message');
const optionsContainerEl = document.getElementById('options-container');
const resultEl = document.getElementById('result');
const scoreEl = document.getElementById('score');
const nextButton = document.getElementById('next-button');

let currentMessage;
let score = 0;
let answered = false;

function getRandomElement(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function displayNewMessage() {
    answered = false;
    nextButton.disabled = true;
    resultEl.textContent = '';
    optionsContainerEl.innerHTML = '';

    currentMessage = getRandomElement(gameData.messages);
    messageEl.textContent = currentMessage.Content;

    const correctAnswer = currentMessage.Author;
    const options = [correctAnswer];

    // Add 3 random wrong answers
    while (options.length < 4) {
        const randomAuthor = getRandomElement(gameData.authors);
        if (!options.includes(randomAuthor)) {
            options.push(randomAuthor);
        }
    }

    // Shuffle options
    for (let i = options.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [options[i], options[j]] = [options[j], options[i]];
    }

    options.forEach(author => {
        const optionDiv = document.createElement('div');
        optionDiv.classList.add('option');
        optionDiv.textContent = author;
        optionDiv.addEventListener('click', () => checkAnswer(author));
        optionsContainerEl.appendChild(optionDiv);
    });
}

function checkAnswer(selectedAuthor) {
    if (answered) return;
    answered = true;

    if (selectedAuthor === currentMessage.Author) {
        resultEl.textContent = 'Correct!';
        resultEl.style.color = 'green';
        score++;
        scoreEl.textContent = score;
    } else {
        resultEl.textContent = `Wrong! The correct author is ${currentMessage.Author}`;
        resultEl.style.color = 'red';
    }

    nextButton.disabled = false;
}

nextButton.addEventListener('click', displayNewMessage);

// Initial load
displayNewMessage();
